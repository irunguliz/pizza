//hovering effect 
$(".image1").mouseover(function () {
    $(".pizza1").show();
    $(".image1").css("opacity", "0.5");
});

$(".image1").mouseout(function () {
    $(".pizza1").hide();
    $(".image1").css("opacity", "1");
});

